number1 = input("Enter in a number:")
number2 = input("Enter in another number:")


print(number1 + "+" + number2 + "=" + str(int(number1) + int(number2)))
print(number1 + "-" + number2 + "=" + str(int(number1) - int(number2)))
print(number1 + "*" + number2 + "=" + str(int(number1) * int(number2)))
print(number1 + "/" + number2 + "=" + str(int(number1) / int(number2)))